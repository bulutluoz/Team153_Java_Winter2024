package day04_matematikselislemler_Increment;

public class C03_Concatenation {

    public static void main(String[] args) {

        /*
            Eger String ile + kullanilirsa
            bu toplama degil birlestirme (concatenation) olarak adlandirilir
         */

        System.out.println( "Ali" + " " + "okula" + " "+ "gitti.");
        // Ali okula gitti.


        System.out.println( "Sayi degeri : " + 2 + 3 ); // "Sayi degeri : 2" + 3
                                                        // "Sayi degeri : 23"


        System.out.println( "Sayi degeri : " + (2 + 3) ); // Sayi degeri : 5

        //System.out.println( "a" - 2);

        //System.out.println( "a" / 2);

        //System.out.println( "a" * 2);

        // String matematiksel islemlere acik degildir
        // sadece + islemini birlestirme(concatenation) olarak kullanabiliriz

    }
}

package day13_forLoop;

public class C11_NestedForLoop {

    public static void main(String[] args) {

        // for loop kullanarak 1 2 3 4 yazdirin
        for (int i = 1; i <=4 ; i++) {
            System.out.print(i*1 + " ");
        }
        System.out.println("");

        // for loop kullanarak 2 4 6 8 yazdirin
        for (int i = 1; i <=4 ; i++) {
            System.out.print(i*2 + " ");
        }
        System.out.println("");

        // for loop kullanarak 3 6 9 12 yazdirin
        for (int i = 1; i <=4 ; i++) {
            System.out.print(i*3 + " ");
        }

        System.out.println("");

        /*
            for loop kullanarak tek seferde
            1 2 3 4
            2 4 6 8
            3 6 9 12
            yazdirin
         */

    }
}
